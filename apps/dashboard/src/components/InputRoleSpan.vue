<template>
  <div>
    <span :class="['flex flex-wrap justify-content-between',
     column ? 'flex-column gap-1' : 'flex-row align-items-center gap-3']">
      <span class="my-0">{{ label }}</span>
      <FindRole  :placeholder="placeholder"
                 v-model:role="internalValue"
                 :default="props.default"
      />
    </span>
    <div class="flex justify-content-end">
      <ErrorSpan :error="errors"/>
    </div>
  </div>
</template>

<script setup lang="ts">
import ErrorSpan from "@/components/ErrorSpan.vue";
import { onMounted, type PropType, ref, watch } from "vue";
import FindRole from "@/components/FindRole.vue";
import { type RoleResponse } from "@sudosos/sudosos-client";

const emit = defineEmits(['update:value']);

const props = defineProps({
  label: {
    type: String,
    required: true
  },
  default: {
    type: Object as PropType<RoleResponse>,
    required: false,
  },
  value: {
    type: Object as PropType<RoleResponse>,
  },
  errors: {
    type: Object as PropType<any>,
    required: false,
  },
  placeholder: {
    type: String,
    required: false,
    default: ''
  },
  disabled: {
    type: Boolean,
    required: false,
    default: false
  },
  column: {
    type: Boolean,
    required: false,
    default: false
  }
});

const internalValue = ref();

onMounted(() => {
  internalValue.value = props.value;
});

watch(() => props.value, (newValue) => {
  internalValue.value = newValue;
});

watch(internalValue, (newValue) => {
  if (newValue !== props.value) {
    emit('update:value', newValue);
  }
});

</script>

